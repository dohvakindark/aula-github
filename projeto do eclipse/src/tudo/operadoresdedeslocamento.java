package tudo;

public class operadoresdedeslocamento {

	public static void main(String[] args) {
	
		int i = 192 >> 1;
		int i = 192 >> 7;
		
		int i = -192 >> 1;
		int i = -192 >> 7;
		
		int i = 192 << 1;
		int i = 192 << 7;
		
		int i = -192 << 1;
		int i = -192 << 7;

		int i = 192 >> 1;
		int i = 192 >> 7;
		
		int i = -192 >>> 1;
		int i = -192 >>> 7;
	}

}
